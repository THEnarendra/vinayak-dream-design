// Helper functions
export const getProjectByRoute = (route) => {
  return projectsData.find(project => project.route === route);
};

export const getProjectsByCategory = (category) => {
  if (category === 'all') return projectsData;
  return projectsData.filter(project => project.category === category);
};

export const getCategories = () => {
  const categories = [
    { id: 'all', name: 'All Projects', count: projectsData.length }
  ];
  
  const uniqueCategories = [...new Set(projectsData.map(project => project.category))];
  
  uniqueCategories.forEach(cat => {
    const count = projectsData.filter(project => project.category === cat).length;
    categories.push({
      id: cat,
      name: cat.charAt(0).toUpperCase() + cat.slice(1),
      count: count
    });
  });
  
  return categories;
};